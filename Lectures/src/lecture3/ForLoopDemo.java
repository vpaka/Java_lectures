package lecture3;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		for(int a=0; a<5; a++)
		{

			System.out.println("the value of a is :"+a);
			
		}
		
		for(int a=10; a>5; a--)
		{

			System.out.println(" a is :"+a);
			
		}
		
		
		

		System.out.println("This is a line outside of loop");
	}
}
